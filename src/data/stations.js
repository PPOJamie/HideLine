export const STATION_BY_ID = new Map(STATIONS.map((station) => [station.id, station]));

export function stationNameLength(name) { return Array.from(String(name || "")).length; }

export function stationLabel(station) {
  if (!station) return "Unknown station";
  const suffix = station.note ? ` - ${station.note}` : station.service ? ` - ${station.service}` : "";
  return `${station.name}${suffix}`;
}

export function findStations(query = "") {
  const needle = String(query).trim().toLowerCase();
  if (!needle) return STATIONS;
  return STATIONS.filter((station) => `${station.name} ${station.service} ${station.note}`.toLowerCase().includes(needle));
}
